    <script>
        // Animation for fade-in elements with hitesh prefix
        document.addEventListener('DOMContentLoaded', function() {
            const fadeElems = document.querySelectorAll('.hitesh-fade-in');
            
            // First, immediately show elements that are already in the viewport on page load
            fadeElems.forEach(elem => {
                // Add active class to all elements immediately visible on page load
                if (isInViewport(elem)) {
                    elem.classList.add('hitesh-active');
                }
            });

            // Then set up the observer for elements that will come into view during scrolling
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('hitesh-active');
                    }
                });
            }, {
                threshold: 0.1
            });

            // Observe all fade elements for scroll-based animation
            fadeElems.forEach(elem => {
                observer.observe(elem);
            });
            
            // Helper function to check if an element is in the viewport
            function isInViewport(element) {
                const rect = element.getBoundingClientRect();
                return (
                    rect.top <= (window.innerHeight || document.documentElement.clientHeight) &&
                    rect.bottom >= 0 &&
                    rect.left <= (window.innerWidth || document.documentElement.clientWidth) &&
                    rect.right >= 0
                );
            }
        });
    </script>
