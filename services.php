    <script>
        // Modified animation for hitesh-fade-in elements
        document.addEventListener('DOMContentLoaded', function() {
            // Immediately add the active class to all fade-in elements
            const fadeElems = document.querySelectorAll('.hitesh-fade-in');
            fadeElems.forEach(elem => {
                elem.classList.add('hitesh-active');
            });
            
            // Then set up the intersection observer for elements that come into view later
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('hitesh-active');
                    }
                });
            }, {
                threshold: 0.1
            });

            fadeElems.forEach(elem => {
                observer.observe(elem);
            });
        });
    </script>
